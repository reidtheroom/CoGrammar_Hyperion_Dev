sentence = "The!quick!brown!fox!jumps!over!the!lazy!dog."
new_sentence = sentence.replace("!"," ") # replaces all ! with whitespace
print(new_sentence)
upper_new_sentence = new_sentence.upper() # change every character to uppercase
print(upper_new_sentence)
reverse_new_sentence = new_sentence[::-1] 
print(reverse_new_sentence) # printing sentence in reverse